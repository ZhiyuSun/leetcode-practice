# Definition for singly-linked list.
class ListNode:
    def __init__(self, x):
        self.val = x
        self.next = None

# 2021.02.09 参考了题解，使用双指针，先走k步数
class Solution:
    def getKthFromEnd(self, head: ListNode, k: int) -> ListNode:
        p = head
        q = head
        while k > 0:
            q = q.next
            k -= 1
        while q:
            q = q.next
            p = p.next
        return p
    
# 别人的解答
class Solution1:
    def getKthFromEnd(self, head: ListNode, k: int) -> ListNode:
        former, latter = head, head
        for _ in range(k):
            former = former.next
        while former:
            former, latter = former.next, latter.next
        return latter

