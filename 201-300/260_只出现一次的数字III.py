import collections
from typing import List

# 2021.04.16 我的垃圾解法
class Solution:
    def singleNumber(self, nums: List[int]) -> List[int]:
        c = collections.Counter(nums)
        res = []
        for i, v in c.items():
            if v == 1:
               res.append(i)
        return res


import functools
