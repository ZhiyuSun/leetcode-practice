from typing import List
# 2021.04.03 自己写出来了，有思路就能做
class Solution:
    def findNumberIn2DArray(self, matrix: List[List[int]], target: int) -> bool:
        if not matrix: return False
        row, col = len(matrix), len(matrix[0])
        i, j = 0, col - 1
        while i < row and j >= 0:
            if matrix[i][j] == target:
                return True
            else:
                if matrix[i][j] < target:
                    i += 1
                else:
                    j -= 1
        return False

