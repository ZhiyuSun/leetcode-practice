class Solution3:
    def removeDuplicates(self, nums: List[int]) -> int:
        if len(nums) < 2: return len(nums)
        i = 0
        j = 1
        while j < len(nums):
            if nums[j] == nums[j-1]:
                j += 1
            else:
                i+=1
                nums[i] = nums[j]
                j+=1
        return i + 1

